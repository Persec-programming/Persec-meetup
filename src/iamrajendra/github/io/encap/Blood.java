package iamrajendra.github.io.encap;

public class Blood {
    private  int id;
    private  int iron;
    private int protien;
    private int fat;

    public int getIron() {
        return iron;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setIron(int iron) {
        this.iron = iron;
    }

    public int getProtien() {
        return protien;
    }

    public void setProtien(int protien) {
        this.protien = protien;
    }

    public int getFat() {
        return fat;
    }

    public void setFat(int fat) {
        this.fat = fat;
    }
}
